import { existsSync } from 'node:fs';
import fs from 'node:fs/promises';
import path from 'node:path';
import { fileURLToPath } from 'node:url';
import process from 'node:process';

// WebView2 is Windows-only; skip entirely on other platforms
if (process.platform !== 'win32') {
  console.log('Skipping WebView2 DLL setup (not Windows)');
  process.exit(0);
}

const __filename = fileURLToPath(import.meta.url);
const root = path.resolve(path.dirname(__filename), '..');
const desktopDir = path.join(root, 'packages', 'desktop');

const releaseDir = path.join(desktopDir, 'src-tauri', 'target', 'release');
const dllSrc = path.join(releaseDir, 'WebView2Loader.dll');
const dllResourceDest = path.join(desktopDir, 'desktop-resources', 'WebView2Loader.dll');

// Copy DLL to desktop-resources/ and generate NSH hook — only if the DLL
// actually exists in the cargo target directory.
if (existsSync(dllSrc)) {
  await fs.mkdir(path.dirname(dllResourceDest), { recursive: true });
  await fs.copyFile(dllSrc, dllResourceDest);
  console.log(`Copied WebView2Loader.dll to desktop-resources/`);

  // Generate NSH hook file so the DLL gets installed alongside the EXE.
  const nshPath = path.join(desktopDir, 'src-tauri', 'includes', 'include-dll.nsh');
  const nshContent = `; Auto-generated by copy-webview2-dll.mjs
; Installs WebView2Loader.dll alongside the EXE.
!macro NSIS_HOOK_POSTINSTALL
  File "/oname=$INSTDIR\\WebView2Loader.dll" "${dllSrc.replace(/\\/g, '\\\\')}"
!macroend
`;
  await fs.mkdir(path.dirname(nshPath), { recursive: true });
  await fs.writeFile(nshPath, nshContent, 'utf-8');
  console.log(`Generated NSH hook: ${nshPath}`);

  // Patch the generated NSIS script to also delete the DLL on uninstall.
  const nsiPath = path.join(desktopDir, 'src-tauri', 'target', 'release', 'nsis', 'x64', 'installer.nsi');
  if (existsSync(nsiPath)) {
    let nsi = await fs.readFile(nsiPath, 'utf-8');
    const delMarker = '  ; Delete external binaries';
    const delLine = `  Delete "$$INSTDIR\\WebView2Loader.dll"`;
    if (nsi.includes(delMarker) && !nsi.includes(delLine)) {
      nsi = nsi.replace(delMarker, `${delLine}\r\n${delMarker}`);
      await fs.writeFile(nsiPath, nsi, 'utf-8');
      console.log('Patched NSIS uninstaller to delete WebView2Loader.dll');
    }
  }
} else {
  console.warn(`WebView2Loader.dll not found at ${dllSrc}, skipping DLL bundle`);
}
